/**
 * Get all contact points for a specific region.
 */
export function getContactPointsByRegion(
  region: CzechRegion,
): HousingContactPoint[] {
  return HOUSING_CONTACT_POINTS.filter((point) => point.region === region);
}

/**
 * Find the nearest contact point for a given region (returns first match).
 * In production, this would use geolocation or user-selected city.
 */
export function getNearestContactPoint(
  region: CzechRegion,
): HousingContactPoint | undefined {
  return HOUSING_CONTACT_POINTS.find((point) => point.region === region);
}

/**
 * Get a contact point by ID.
 */
export function getContactPointById(
  id: string,
): HousingContactPoint | undefined {
  return HOUSING_CONTACT_POINTS.find((point) => point.id === id);
}

/**
 * Get all available regions.
 */
export function getAllRegions(): CzechRegion[] {
  return Object.keys(REGION_NAMES) as CzechRegion[];
}

/**
 * Get count of contact points by region.
 */
export function getContactPointCountByRegion(): Record<CzechRegion, number> {
  const counts: Partial<Record<CzechRegion, number>> = {};
  for (const region of getAllRegions()) {
    counts[region] = getContactPointsByRegion(region).length;
  }
  return counts as Record<CzechRegion, number>;
}
